package method;

public class Ex3 {

	// 숫자1부터 10까지 출력하는 함수 선언
	public static void printTen() {
		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
		}
	}
	public static void main(String[] args) {
		printTen(); //함수 호출
		printTen(); //함수 호출
		printTen(); //함수 호출
		System.out.println();
	}


}
